# This is the function to measure source acoustics using default male parameters.
def measurePitch(voiceID, f0min, f0max, unit):
    sound = parselmouth.Sound(voiceID) # read the sound
    duration = call(sound, "Get total duration") # duration
    pitch = call(sound, "To Pitch", 0.0, f0min, f0max) #create a praat pitch object
    meanF0 = call(pitch, "Get mean", 0, 0, unit) # get mean pitch
    stdevF0 = call(pitch, "Get standard deviation", 0 ,0, unit) # get standard deviation
    # harmonicity = call(sound, "To Harmonicity (cc)", 0.01, f0min, 0.1, 1.0)
    # hnr = call(harmonicity, "Get mean", 0, 0)

    return duration, meanF0, stdevF0
# calculating Zero Crossing Rate for entire signal
def measureZCR(x):
    ZCR=librosa.feature.zero_crossing_rate(x, frame_length = len(x), hop_length = len(x))

    return ZCR

# calculating RMS energy for entire signal
def measureRMSE(x):
    rms_energy = librosa.feature.rmse(x, frame_length = len(x), hop_length = len(x), pad_mode = 'reflect')

    return rms_energy

# calculating MFCCs (with window width of 0.15sec and frame_stride of 20msec)  --- framing and windowing are included
def measureMFCC(x, sr):
    mfcc_f= librosa.feature.mfcc(x, sr=sr, n_mfcc = 12, n_fft =int(0.02*sr), hop_length =int(0.15*sr))

    return mfcc_f
